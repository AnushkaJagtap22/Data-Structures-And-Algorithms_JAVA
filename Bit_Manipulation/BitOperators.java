package Bit_Manipulation;

public class BitOperators {
    public static void main(String[] args) {
        System.out.println("Bitwise AND of 5 & 3: " + (5 & 3));
        System.out.println("Bitwise OR of 5 | 3: " + (5 | 3));
        System.out.println("Bitwise XOR of 5 ^ 3: " + (5 ^ 3));
        System.out.println("Left Shift of 5 << 1: " + (5 << 1));
        System.out.println("Right Shift of 5 >> 1: " + (5 >> 1));
        System.out.println("One's Complement of ~5: " + (~5));
    }
}
